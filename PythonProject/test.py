@app.route("/quiz", methods=["GET","POST"])
def quiz():
    if request.method=="POST":
        result={"А":0,"Б":0,"В":0}
        for i,q in enumerate(questions):
            ans=request.form.get(f"q{i}")
            if ans in q["answers"]:
                idx=q["answers"].index(ans)
                result[q["types"][idx]]+=1
        thinking=max(result,key=result.get)
        stats={"a":result["А"],"b":result["Б"],"c":result["В"]}
        return render_template("result.html",
                               title=descriptions[thinking]["title"],
                               text=descriptions[thinking]["text"],
                               stats=stats)
    return render_template("quiz.html", questions=questions)

